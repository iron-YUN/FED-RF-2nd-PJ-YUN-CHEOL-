//  공통처리 JS - common.js

// 나의 함수 불러오기
import mFn from "./my_function.js";

// 공통처리 데이터 불러오기
import comData from "../data/common_data.js";

export default function setElement() {
  ////////////////////////////////////////////////////////////////////////////////////
  // 공통영역 불러오기 /////////////////
  const topArea = mFn.qs("#top-area");
  const footerArea = mFn.qs("#footer-area");

  topArea.innerHTML = comData.topArea;
  footerArea.innerHTML = comData.footerArea;
  ////////////////////////////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////스크롤바 없애기//////////////////////////////////////////////////
const btnMenu = mFn.qs(".btnMenu");
const closeMenu = mFn.qs(".menu-close");
const gnb = mFn.qs(".gnb");
mFn.addEvt(btnMenu, "click", function () {
  gnb.classList.add("active");
  mFn.qs("body").classList.add("hidden");
});
mFn.addEvt(closeMenu, "click", function () {
  gnb.classList.remove("active");
  mFn.qs("body").classList.remove("hidden");
});
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

///////////////////////////////동일 버튼요소 뿌려주기////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////
const buttonBtn = mFn.qsa(".button-btn");
buttonBtn.forEach((button) => {
  button.innerHTML = `
        <svg>
            <path
                fill="currentColor"
                d="M24.887 15.5 10 6.196v18.608l4.667-2.917-1-1.733L12 21.196V9.804l9.113 5.696-5.75 3.594 1.001 1.733 8.523-5.327Z"
            ></path>
        </svg>
    `;
});
////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////

} ////////////// setElement 함수 ////////////




